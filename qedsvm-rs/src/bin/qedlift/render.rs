//! Lean source rendering helpers for qedlift.
//!
//! The lifter computes symbolic state; this module owns the larger Lean
//! document templates so the walker stays focused on control-flow and proof
//! data. Small expression renderers still live next to their typed ASTs.

use std::path::Path;

pub(super) fn decode_claim(emit_decode_bridge: bool) -> &'static str {
    if emit_decode_bridge {
        "1. The .text bytes are embedded verbatim as a `ByteArray`.\n\
         2. `Decode.decodeProgram` recovers the instruction sequence;\n\
            `native_decide` proves the decode is correct.\n"
    } else {
        "1. The .text bytes are embedded as a hex string\n\
            (`Decode.bytesOfHex`; a `ByteArray` literal this large\n\
            blows `maxRecDepth`).\n\
         2. The full `decodeProgram` bridge is omitted; instead the\n\
            `_decode_pins` theorem pins the decode of every walked\n\
            PC's bytes by `native_decide`, threading the V0 function\n\
            registry so internal `call_local` PCs resolve (audit H2).\n"
    }
}

pub(super) fn module_intro(
    so_path: &Path,
    decode_claim: &str,
    so_stem: &str,
    module_name: &str,
    // Shared-text base (batch dedup): `Some("PToken")` adds
    // `import Generated.PTokenText` so the decode pins resolve the shared defs.
    shared_text: Option<&str>,
) -> String {
    let mut out = String::new();
    out.push_str(&format!(
        "/-\n  Generated by `qedlift` from `{}`.\n\
         \n\
         End-to-end lift demonstration:\n\
         {}\
         3. A `cuTripleWithinMem` Hoare triple is stated over the\n\
            decoded sequence. The pre/post atom synthesis is the next\n\
            iteration's work (the \"symbolic executor\" piece); for the\n\
            demo, see the worked example in\n\
            `SVM/SBPF/Macros.lean` (`{}_macro_spec_auto`) where the\n\
            theorem is proved by `sl_block_auto` against the same\n\
            instruction sequence.\n\
         -/\n\n",
        so_path.display(),
        decode_claim,
        so_stem,
    ));
    out.push_str("import SVM.SBPF.Decode\n");
    out.push_str("import SVM.SBPF.RunnerBridge\n");
    out.push_str("import SVM.SBPF.Macros\n");
    out.push_str("import SVM.SBPF.SatWitness\n");
    if let Some(base) = shared_text {
        out.push_str(&format!("import Generated.{}Text\n", base));
    }
    out.push('\n');
    out.push_str("set_option maxRecDepth 65536\n");
    out.push_str("set_option maxHeartbeats @@HEARTBEATS@@\n\n");
    out.push_str(&format!("namespace Examples.Lifted.{}\n\n", module_name));
    out.push_str("open SVM.SBPF\n\n");
    out
}

pub(super) fn decode_bridge_omitted_note(
    module_name: &str,
    text_len: usize,
    shared_text: Option<&str>,
) -> String {
    let text_ref = match shared_text {
        Some(base) => format!("`{}Text`, shared module `Generated.{}Text`", base, base),
        None => format!("`{}Text`", module_name),
    };
    format!(
        "-- NOTE: `{}Bytes` + `{}Insns` + `{}_decodes` omitted — the .text\n\
         -- is {} bytes, which blows `maxRecDepth` as a ByteArray literal.\n\
         -- The byte→insn link is pinned instead by `{}_decode_pins` below:\n\
         -- the `.text` is embedded as a hex string ({}) and every\n\
         -- walked PC's decode is checked by `native_decide` against the\n\
         -- exact instruction the Hoare triple's `CodeReq` claims there\n\
         -- (soundness-audit H8).\n\n",
        module_name, module_name, module_name, text_len, module_name, text_ref,
    )
}

pub(super) fn text_bytearray_defs(
    module_name: &str,
    text_bytes: &[u8],
    text_offset: u64,
) -> String {
    let mut out = String::new();
    out.push_str("/-- `.text` bytes extracted from the .so by qedlift. -/\n");
    out.push_str(&format!("def {}Bytes : ByteArray := ⟨#[\n", module_name));
    for (i, byte) in text_bytes.iter().enumerate() {
        if i % 8 == 0 {
            out.push_str("  ");
        }
        out.push_str(&format!("0x{:02x}", byte));
        if i + 1 < text_bytes.len() {
            out.push_str(", ");
        }
        if i % 8 == 7 || i + 1 == text_bytes.len() {
            out.push('\n');
        }
    }
    out.push_str("]⟩\n\n");
    out.push_str(&format!(
        "/-- Text section file-offset: 0x{:x}. -/\n",
        text_offset
    ));
    out.push_str(&format!(
        "def {}TextOffset : Nat := 0x{:x}\n\n",
        module_name, text_offset
    ));
    out
}

pub(super) fn decode_renderer_skip_note(module_name: &str, reason: &str) -> String {
    format!(
        "-- NOTE: `{}Insns` + `{}_decodes` omitted — `.text` contains an\n\
         -- opcode the renderer doesn't model yet ({}). The Hoare\n\
         -- triple below is unaffected: its `CodeReq` references only\n\
         -- the walked-arm PCs, not the full `.text`.\n\n",
        module_name, module_name, reason,
    )
}

pub(super) fn decoded_insns_section(
    module_name: &str,
    rendered_insns: &[String],
    fn_registry_lean: &str,
) -> String {
    let mut out = String::new();
    out.push_str("/-- Decoded form of the .text bytes. -/\n");
    out.push_str(&format!("def {}Insns : Array Insn := #[\n", module_name));
    for (i, lean) in rendered_insns.iter().enumerate() {
        let sep = if i + 1 < rendered_insns.len() {
            ","
        } else {
            ""
        };
        out.push_str(&format!("  {}{}\n", lean, sep));
    }
    out.push_str("]\n\n");
    out.push_str(
        "/-- The V0 function registry (murmur3 key → target slot) \
         solana-sbpf built\n    at load, mirroring `Elf.buildFnRegistry` \
         (audit H2). Resolves internal\n    `call` immediates to \
         `.call_local` targets. -/\n",
    );
    out.push_str(&format!(
        "def {}FnRegistry : List (Nat × Nat) := {}\n\n",
        module_name, fn_registry_lean
    ));
    out.push_str("/-- The bytes decode exactly to the expected instruction array. -/\n");
    out.push_str(&format!(
        "theorem {}_decodes :\n    \
         Decode.decodeProgram {}Bytes {}FnRegistry = some {}Insns := by\n  \
         native_decide\n\n",
        module_name, module_name, module_name, module_name,
    ));
    out
}

/// The `{base}Text` + `{base}SlotMap` + `{base}FnRegistry` defs the decode
/// pins reference. `pins_where` names where the pins live ("the pins below"
/// inline; "the per-arm pins" in the shared-text module).
pub(super) fn text_defs(
    base: &str,
    text_bytes: &[u8],
    fn_registry_lean: &str,
    pins_where: &str,
) -> String {
    let mut out = String::new();
    out.push_str(&format!(
        "/-- The `.text` bytes of the binary, embedded as hex \
         (whitespace-insensitive;\n    see `Decode.bytesOfHex`). \
         {} bytes. -/\n",
        text_bytes.len()
    ));
    out.push_str(&format!(
        "def {}Text : ByteArray := Decode.bytesOfHex \"\n",
        base
    ));
    for chunk in text_bytes.chunks(64) {
        let line: String = chunk.iter().map(|b| format!("{:02x}", b)).collect();
        out.push_str(&format!("  {}\n", line));
    }
    out.push_str("  \"\n\n");
    out.push_str(&format!(
        "/-- Byte-slot → logical-PC map for `{}Text` (pass 1 of the \
         decoder),\n    computed in-kernel — jump-target resolution \
         in {} depends on\n    the `lddw` positions of \
         the WHOLE text, not just the pinned windows. -/\n",
        base, pins_where
    ));
    out.push_str(&format!(
        "def {}SlotMap : Array Nat := Decode.buildSlotMap {}Text\n\n",
        base, base
    ));
    out.push_str(&format!(
        "/-- The V0 function registry (murmur3 key → target slot) \
         solana-sbpf built\n    at load, mirroring `Elf.buildFnRegistry` \
         (audit H2). Resolves internal\n    `call` immediates in {} \
         to `.call_local` targets. -/\n",
        pins_where
    ));
    out.push_str(&format!(
        "def {}FnRegistry : List (Nat × Nat) := {}\n\n",
        base, fn_registry_lean
    ));
    out
}

/// The `{module_name}_decode_pins` theorem, referencing the text defs of
/// `text_base` (== `module_name` inline; the shared base in shared mode).
pub(super) fn decode_pins_theorem(
    module_name: &str,
    text_base: &str,
    pin_offsets: &[String],
    pin_expected: &[String],
) -> String {
    let mut out = String::new();
    out.push_str(&format!(
        "/-- Walked-PC decode pins (soundness-audit H8 + H2): every \
         instruction the\n    Hoare triple's `CodeReq` claims at a \
         walked PC is the decode of the\n    `.text` bytes at that \
         PC's byte offset, checked end-to-end in-kernel\n    \
         (`bytesOfHex` → `buildSlotMap` → `decodeInsn` with the \
         function registry) by\n    `native_decide`. Internal \
         `call_local` PCs are included — their murmur3\n    imm \
         resolves through `{}FnRegistry`. -/\n",
        text_base
    ));
    out.push_str(&format!("theorem {}_decode_pins :\n    ([", module_name));
    for (i, off) in pin_offsets.iter().enumerate() {
        if i > 0 {
            out.push_str(", ");
            if i % 16 == 0 {
                out.push_str("\n      ");
            }
        }
        out.push_str(off);
    }
    out.push_str(&format!(
        "].map fun off =>\n      Decode.decodeInsn {}Text {}SlotMap off \
         {}FnRegistry) = [\n",
        text_base, text_base, text_base
    ));
    for (i, exp) in pin_expected.iter().enumerate() {
        let sep = if i + 1 < pin_expected.len() { "," } else { "" };
        out.push_str(&format!("      {}{}\n", exp, sep));
    }
    out.push_str("    ] := by\n  native_decide\n\n");
    out
}

pub(super) fn large_text_decode_section(
    module_name: &str,
    text_bytes: &[u8],
    fn_registry_lean: &str,
    pin_offsets: &[String],
    pin_expected: &[String],
) -> String {
    let mut out = text_defs(module_name, text_bytes, fn_registry_lean, "the pins below");
    out.push_str(&decode_pins_theorem(module_name, module_name, pin_offsets, pin_expected));
    out
}

/// The shared `.text` module (batch dedup): one binary's `{base}Text` +
/// `{base}SlotMap` + `{base}FnRegistry`, emitted ONCE as
/// `Generated/{base}Text.lean` and imported by every per-arm lift of that
/// binary — the per-arm `*_decode_pins` theorems reference these defs.
pub(super) fn shared_text_module(
    base: &str,
    so_path: &Path,
    text_bytes: &[u8],
    fn_registry_lean: &str,
) -> String {
    let mut out = String::new();
    out.push_str(&format!(
        "/-\n  Generated by `qedlift` from `{}`.\n\
         \n\
         Shared `.text` embedding: the binary's `.text` bytes, byte-slot map\n\
         and V0 function registry, emitted ONCE and imported by every per-arm\n\
         lift of this binary (each arm's `*_decode_pins` theorem references\n\
         these defs — soundness-audit H8 + H2). Deduplicates the {}-byte\n\
         blob across the arm modules.\n\
         -/\n\n",
        so_path.display(),
        text_bytes.len()
    ));
    out.push_str("import SVM.SBPF.Decode\n\n");
    out.push_str("namespace Examples.Lifted\n\n");
    out.push_str("open SVM.SBPF\n\n");
    out.push_str(&text_defs(base, text_bytes, fn_registry_lean, "the per-arm pins"));
    out.push_str("end Examples.Lifted\n");
    out
}

pub(super) fn lifted_triple_section_header() -> &'static str {
    "/-! ## Symbolically lifted Hoare triple\n\n\
     Synthesised by qedlift's symbolic executor walking the\n\
     decoded insns left-to-right. Closed by `sl_block_auto`. -/\n\n"
}

pub(super) struct TripleTheorem<'a> {
    pub name: &'a str,
    pub binders: &'a str,
    pub n: usize,
    pub m_bound: &'a str,
    pub start_pc: usize,
    pub exit_pc: usize,
    pub cr: &'a str,
    pub pre: &'a str,
    pub post: &'a str,
    pub rr: &'a str,
    pub proof: &'a str,
}

pub(super) fn cu_triple_theorem(t: &TripleTheorem<'_>) -> String {
    format!(
        "open Memory in\n\
         theorem {}\n    {}: \
         cuTripleWithinMem {} {} {} {}\n      \
         ({})\n      \
         ({})\n      \
         ({})\n      \
         (fun rt => {}) := by\n\
         {}\n\n",
        t.name,
        t.binders,
        t.n,
        t.m_bound,
        t.start_pc,
        t.exit_pc,
        t.cr,
        t.pre,
        t.post,
        t.rr,
        t.proof,
    )
}

/// A `cuTripleFaultsWithinMem` theorem — the typed-fault corollary shape
/// (Phase 7 sub-item 3). No post-state (the run is stuck once it faults); the
/// terminal carries a `VmError` (`vm_error`, e.g. `.abort`) instead.
pub(super) struct FaultsTriple<'a> {
    pub name: &'a str,
    pub binders: &'a str,
    /// Step bound, rendered (`N1 + N2`, e.g. `"2 + 1"`).
    pub n_steps: &'a str,
    /// CU bound, rendered (`M1 + M2`, e.g. `"0 + nCuAbort"`).
    pub n_cu: &'a str,
    pub entry: usize,
    pub cr: &'a str,
    pub pre: &'a str,
    pub rr: &'a str,
    pub vm_error: &'a str,
    pub proof: &'a str,
}

pub(super) fn faults_triple_theorem(t: &FaultsTriple<'_>) -> String {
    format!(
        "open Memory in\n\
         theorem {}\n    {}: \
         cuTripleFaultsWithinMem ({}) ({}) {}\n      \
         ({})\n      \
         ({})\n      \
         (fun rt => {})\n      \
         {} := by\n\
         {}\n\n",
        t.name,
        t.binders,
        t.n_steps,
        t.n_cu,
        t.entry,
        t.cr,
        t.pre,
        t.rr,
        t.vm_error,
        t.proof,
    )
}

/// Proof body of the `<module>_allocates` heap corollary: unfold the heap
/// predicates, then close by the lifted spec applied at its binders.
pub(super) fn heap_alloc_proof(module_name: &str, names: &str) -> String {
    format!(
        "  simp only [heapBumpPtr, heapBlockU64]\n  exact {}_lifted_spec {}",
        module_name, names,
    )
}

/// Proof body of the `<module>_balance_correct` corollary: rewrite the wrap
/// arithmetic to clean Nat ops under the guard hypotheses.
pub(super) fn balance_proof(module_name: &str, names: &str, rw_terms: &str) -> String {
    format!(
        "  have h := {}_lifted_spec {}\n  rw [{}]\n  exact h",
        module_name, names, rw_terms,
    )
}

pub(super) fn end_namespace(module_name: &str) -> String {
    format!("end Examples.Lifted.{}\n", module_name)
}
